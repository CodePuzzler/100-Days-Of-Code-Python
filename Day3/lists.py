# Day3 of my 100DaysOfCode Challenge
# Program to use various List methods

list1 = [1, 67, 87, 5, 20, 11]

# sorting a list
list1.sort()
print(list1)

# reverses a list
list1.reverse()
print(list1)

# add an element at the end
list1.append(90)
print(list1)

# inserts an  element at given index
# index is 3 and element is 33
list1.insert(3, 33)
print(list1)

# pops an element from an index
list1.pop(2)
print(list1)

# removes an element
list1.remove(33)
print(list1)